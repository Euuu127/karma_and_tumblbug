package com.karma_and_tumblbug.p1.project;

public class GiftDTO {

	private long num;
	private long giftNum;
	private long price;
	private String giftName;
	private String giftSummary;
	public long getNum() {
		return num;
	}
	public void setNum(long num) {
		this.num = num;
	}
	public long getGiftNum() {
		return giftNum;
	}
	public void setGiftNum(long giftNum) {
		this.giftNum = giftNum;
	}
	public long getPrice() {
		return price;
	}
	public void setPrice(long price) {
		this.price = price;
	}
	public String getGiftName() {
		return giftName;
	}
	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}
	public String getGiftSummary() {
		return giftSummary;
	}
	public void setGiftSummary(String giftSummary) {
		this.giftSummary = giftSummary;
	}
	
}
